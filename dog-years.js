const myAge = 33 // my age
let earlyYears = 2 // dog ages rapidly first two years
earlyYears = earlyYears * 10.5
let laterYears = myAge - 2 // we already accounted for the first two years with earlyYears
laterYears = laterYears * 4 // to account for the later years
// console.log(earlyYears);
// console.log(laterYears);
myAgeInDogYears = earlyYears + laterYears // adding together earlyYears plus laterYears to get my age in dog years
let myName = "Gary"
myName = myName.toLowerCase() // storing my name in all lower case
// console.log(myName);
console.log("My name is " + myName + ". I am " + myAge + " years old in human years which is " + myAgeInDogYears + " years old in dog years.");
